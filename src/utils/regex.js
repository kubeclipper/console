/* eslint-disable no-useless-escape */
// FQDN
export const fqdn =
  /^([a-zA-Z0-9]{1}[a-zA-Z0-9_-]{0,62})(\.[a-zA-Z0-9_]{1}[a-zA-Z0-9_-]{0,62})*?(\.[a-zA-Z]{1}[a-zA-Z0-9]{0,62})\.?$/;
// ipv4 cidir
export const ipCidr =
  /^([0-9]{1,3}\.){3}[0-9]{1,3}(\/([0-9]|[1-2][0-9]|3[0-2]))?$/;
// port: 1-65535
export const port =
  /^()([1-9]|[1-5]?[0-9]{2,4}|6[1-4][0-9]{3}|65[1-4][0-9]{2}|655[1-2][0-9]|6553[1-5])$/;
// URL checker
export const url =
  /^(http:\/\/www\.|https:\/\/www\.|http:\/\/|https:\/\/)?[a-z0-9]+([\-\.]{1}[a-z0-9]+)*\.[a-z]{2,5}(:[0-9]{1,5})?(\/.*)?$/;
// k8s version
export const k8sVersion = /^(\d+\.)?(\d+\.)?(\*|\d+)$/;

export const namespace = /^[a-z0-9]([-a-z0-9]*[a-z0-9])?$/;

export const storageClassName = /^[a-z0-9]([-a-z0-9]*[a-z0-9])?$/;

export const domain =
  /^(?!:\/\/)([a-zA-Z0-9-_]+\.)*[a-zA-Z0-9][a-zA-Z0-9-_]+\.[a-zA-Z]{2,11}?$/;

export const subdomain =
  /^[a-z0-9]([-a-z0-9]*[a-z0-9])?(\.[a-z0-9]([-a-z0-9]*[a-z0-9])?)*$/;

export const path = /^\/([a-zA-Z0-9-_]+\/?)+$/;
